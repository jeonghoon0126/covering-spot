-- ------------------------------------------------------------
-- 1. spot_items
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS spot_items (
  id           UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  category     TEXT        NOT NULL,
  name         TEXT        NOT NULL,
  display_name TEXT        NOT NULL,
  price        INTEGER     NOT NULL,
  loading_cube NUMERIC(6,3) NOT NULL,
  active       BOOLEAN     NOT NULL DEFAULT true,
  created_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT uq_spot_items_category_name UNIQUE (category, name)
);

ALTER TABLE spot_items DISABLE ROW LEVEL SECURITY;

CREATE INDEX IF NOT EXISTS idx_spot_items_category ON spot_items (category);
CREATE INDEX IF NOT EXISTS idx_spot_items_active   ON spot_items (active);

-- ------------------------------------------------------------
-- 2. spot_areas
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS spot_areas (
  id         UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  name       TEXT        NOT NULL UNIQUE,
  price1     INTEGER     NOT NULL,
  price2     INTEGER     NOT NULL,
  price3     INTEGER     NOT NULL,
  active     BOOLEAN     NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE spot_areas DISABLE ROW LEVEL SECURITY;

CREATE INDEX IF NOT EXISTS idx_spot_areas_name ON spot_areas (name);

-- ------------------------------------------------------------
-- 3. spot_ladder
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS spot_ladder (
  id         UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  type       TEXT        NOT NULL,
  duration   TEXT        NOT NULL,
  price      INTEGER     NOT NULL,
  sort_order INTEGER     NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT uq_spot_ladder_type_duration UNIQUE (type, duration)
);

ALTER TABLE spot_ladder DISABLE ROW LEVEL SECURITY;

