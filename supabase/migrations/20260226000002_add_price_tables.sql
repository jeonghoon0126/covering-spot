-- category + name → spot_items(category, name) 참조
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS spot_item_aliases (
  id         UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  alias      TEXT NOT NULL UNIQUE,
  category   TEXT NOT NULL,
  name       TEXT NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT fk_alias_item FOREIGN KEY (category, name)
    REFERENCES spot_items (category, name) ON DELETE CASCADE
);

ALTER TABLE spot_item_aliases DISABLE ROW LEVEL SECURITY;

CREATE INDEX IF NOT EXISTS idx_spot_item_aliases_alias ON spot_item_aliases (alias);

